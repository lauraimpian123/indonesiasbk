// Helper function to get reviews by category
export function getReviewsByCategory(category: 'bike' | 'gear' | 'helmet' | 'accessory' | 'comparison'): Review[] {
  switch (category) {
    case 'bike':
      return bikeReviews;
    case 'gear':
      return gearReviews;
    case 'helmet':
      return helmetReviews;
    case 'accessory':
      return accessoryReviews;
    case 'comparison':
      return comparisonReviews;
    default:
      return [];
  }
}

// Helper function to get review by ID
export function getReviewById(id: string): Review | undefined {
  const allReviews = [...bikeReviews, ...gearReviews, ...helmetReviews, ...accessoryReviews, ...comparisonReviews];
  return allReviews.find((review) => review.id === id);
}

// Helper function to get related products
export function getRelatedProducts(productId: string): Review[] {
  const product = getReviewById(productId);
  if (!product) return [];
  
  const allReviews = [...bikeReviews, ...gearReviews, ...helmetReviews, ...accessoryReviews];
  return allReviews.filter((review) => product.relatedProducts.includes(review.id)).slice(0, 4);
}

// Helper function to get comments for a product
export function getComments(productId: string): Comment[] {
  return mockComments[productId] || [];
}

